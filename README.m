# Learning Management System

## Project Overview
The Learning Management System (LMS) is a full-stack web application that enables students to access courses, submit assignments, and track progress while administrators manage educational content.

## Features
- User Authentication
- Course Management
- Assignment Submission
- Student Dashboard
- Admin Dashboard
- Progress Tracking

## Technologies Used
### Frontend
- HTML
- CSS
- JavaScript
- React.js

### Backend
- Node.js
- Express.js

### Database
- MongoDB

## Installation

### Frontend
cd frontend
npm install
npm start

### Backend
cd backend
npm install
npm start

